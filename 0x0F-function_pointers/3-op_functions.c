#include "3-calc.h"
/**
 * op_add - function that adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: product of two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: division of two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function of modulus between two integers
 * @a: first integer
 * @b: second integer
 * Return: The remainder of modulus between two integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
